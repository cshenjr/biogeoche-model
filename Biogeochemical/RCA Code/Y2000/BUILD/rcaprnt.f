      SUBROUTINE  RCAPRNT(ARRAY,IBEG,IEND)
C
C          RCAPRNT IS A GENERAL PRINT ROUTINE FOR ARRAYS
C
      SAVE
      INCLUDE  'RCACM'
      REAL   ARRAY(NX,NY,NZ)

      WRITE(OUT,1000)
 1000 FORMAT(1X,'LAYER  COLUMN',46X,'ROWS')
      DO 40 IZAY=IBEG,IEND
      DO 40 IY=1,NY
      WRITE(OUT,1100)  IZAY,IY,(I,I=1,10)
 1100 FORMAT(//1X,I3,I8,2X,10I10)
      WRITE(OUT,1200)   (ARRAY(IX,IY,IZAY),IX=1,NX)
 1200 FORMAT(14X,10E10.3)
   40 CONTINUE

      RETURN
      END
